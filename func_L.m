%% L TENSOR
% 6x6 Tensor
function [L1p, L2p]=func_L(A)

% NOTE L1 is L prime. L2 is L double prime. 
L1p(1,1) = 2*A(1)/3;
L1p(1,2) =  -A(1)/3;
L1p(2,1) =  -A(2)/3;
L1p(2,2) = 2*A(2)/3;
L1p(6,6) = A(7);
L1p(3,1) =-L1p(1,1)-L1p(2,1);
L1p(3,2) =-L1p(1,2)-L1p(2,2);
L1p(4,4) = 1.0;
L1p(5,5) = 1.0;
L1p(1,3) =  -A(1)/3;
L1p(2,3) =  -A(2)/3;
L1p(3,3) =-L1p(1,3)-L1p(2,3);
	  
% L2
L2p(1,1) = (8*A(5)-2*A(3)-2*A(6)+2*A(4))/9;
L2p(1,2) = (4*A(6)-4*A(4)-4*A(5)+  A(3))/9;
L2p(2,1) = (4*A(3)-4*A(5)-4*A(4)+  A(6))/9;
L2p(2,2) = (8*A(4)-2*A(6)-2*A(3)+2*A(5))/9;
L2p(6,6) = A(8);
L2p(3,1) =-L2p(1,1)-L2p(2,1);
L2p(3,2) =-L2p(1,2)-L2p(2,2);
L2p(4,4) = 1.0;
L2p(5,5) = 1.0;
L2p(1,3) = (A(3)-4*A(5)+2*A(4)-2*A(6))/9;
L2p(2,3) = (2*A(5)-2*A(3)+A(6)-4*A(4))/9;
L2p(3,3) =-L2p(1,3)-L2p(2,3);
end
